// Get content by ID
export const getContentById = (id) => {
    const allContent = [featuredContent, ...trendingNow, ...popularAnime, ...newReleases, ...topRatedMovies];
    return allContent.find(item => item.id === parseInt(id));
};

// Get related content
export const getRelatedContent = (id, type) => {
    const sourceArray = type === 'anime' ? allAnime : allMovies;
    return sourceArray.filter(item => item.id !== parseInt(id)).slice(0, 6);
};

